// Enhanced animations with AOS initialization
document.addEventListener('DOMContentLoaded', function() {
  // Initialize AOS
  AOS.init({
    duration: 800,
    easing: 'ease-in-out',
    once: true,
    mirror: false
  });

  // Smooth scroll for anchor links
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
      e.preventDefault();
      const target = document.querySelector(this.getAttribute('href'));
      if (target) {
        window.scrollTo({
          top: target.offsetTop - 100,
          behavior: 'smooth'
        });
      }
    });
  });

  // Floating animation for elements with .floating class
  const floatingElements = document.querySelectorAll('.floating');
  floatingElements.forEach(el => {
    el.style.animationDelay = el.classList.contains('delay-1') ? '0.2s' : 
                            el.classList.contains('delay-2') ? '0.4s' : 
                            el.classList.contains('delay-3') ? '0.6s' : 
                            el.classList.contains('delay-4') ? '0.8s' : 
                            el.classList.contains('delay-5') ? '1s' : '0s';
  });

  // Scroll reveal for content blocks
  const contentBlocks = document.querySelectorAll('.content-block');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animated');
      }
    });
  }, { threshold: 0.1 });

  contentBlocks.forEach(block => {
    observer.observe(block);
  });
});
